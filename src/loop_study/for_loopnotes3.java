
package loop_study;

public class print_1_to_100 {

	public static void main(String[] args) 
	{
	//print number form 1-100	

		//for loop  1--->100
		//0-->99
		
//		1-->3
//		i=i+2
		
		for(int i=1;i<=100;i++)//1,2,3-----100
		{
			System.out.println("Value of i is "+i);
		}
		
		for(int i=100;i>=1;i=i-2)
		{
			System.out.println("value of i is "+i);
		}
	}

}


